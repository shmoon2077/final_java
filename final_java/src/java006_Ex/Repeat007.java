package java006_Ex;

public class Repeat007 {
	public static void main (String [] args) {
	int a=0;
	
	for (a=1;a<=5;a++) {System.out.print(a +"\t");};
	System.out.println();

	int b=1;
	while (b<=5) {System.out.print(b+"\t"); b++;};
	System.out.println();

	int c=1;
	do {System.out.print(c+"\t"); c++;} while (c<=5);
	System.out.println();
	
	
	int d = 0;
	for (d=5;d>=1;d--) {System.out.print(d+"\t");};
	System.out.println();
	int e = 5;
	while (e>=1) {System.out.print(e+"\t"); e--;};
	System.out.println();
	int f = 5;
	do {System.out.print(f+"\t"); f--;} while (f>=1);
	System.out.println();
	
	
	int g=0;
	for (g=1;g<=3;g++) {System.out.print("JAVA"+g+"\t");};
	System.out.println();
	int h=1;
	while (h<=3) {System.out.print("JAVA"+h+"\t"); h++;};
	System.out.println();
	int i=1;
	do {System.out.print("JAVA"+i+"\t"); i++;} while (i<=3);
	
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
