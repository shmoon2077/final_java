package java006;

public class Repeat002_while_dowhile {
	public static void main (String [] args) {
		// for (반복 횟수를 알 때)
		
		for (int i=1; i<=3; i++) {System.out.print( i + "\t");};
		
		
		
		// while (반복횟수를 모를 때) for -> while 바꾸는 과정
		for (int i=1; i<=3; i++) {System.out.print( i + "\t");}
		
		int a =1;
		while (a<=3) { a++ ; System.out.print( a + "\t"); }
		
		
		
		// do while (무조건 1번은 실행해야할 때)
		
		
		
		
		
		
			
	} // end main
} // end class
